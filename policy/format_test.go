func TestToSARIFResults(t *testing.T) {
	doc := testDoc()
	out, _ := doc.ToSARIF("1.0.0")

	var report SARIFReport
	json.Unmarshal(out, &report)

	results := report.Runs[0].Results
	// testDoc has 2 non-wildcard actions = 2 permission-detected results
	if len(results) != 2 {
		t.Fatalf("Results = %d, want 2", len(results))
	}
	for _, r := range results {
		if r.RuleID != "iampg/permission-detected" {
			t.Errorf("RuleID = %q, want iampg/permission-detected", r.RuleID)
		}
		if r.Level != "note" {
			t.Errorf("Level = %q, want note", r.Level)
		}
	}
}

func TestToSARIFWildcardDetection(t *testing.T) {
	doc := &Document{
		Version: "2012-10-17",
		Statement: []Statement{
			{Effect: "Allow", Action: []string{"s3:*"}, Resource: "*"},
		},
	}

	out, _ := doc.ToSARIF("1.0.0")
	var report SARIFReport
	json.Unmarshal(out, &report)

	results := report.Runs[0].Results
	// Should have: 1 wildcard-action + 1 wildcard-resource
	hasWildcardAction := false
	hasWildcardResource := false
	for _, r := range results {
		if r.RuleID == "iampg/wildcard-action" {
			hasWildcardAction = true
		}
		if r.RuleID == "iampg/wildcard-resource" {
			hasWildcardResource = true
		}
	}
	if !hasWildcardAction {
		t.Error("Missing wildcard-action result")
	}
	if !hasWildcardResource {
		t.Error("Missing wildcard-resource result")
	}
}

func TestToSARIFRules(t *testing.T) {
	doc := testDoc()
	out, _ := doc.ToSARIF("1.0.0")

	var report SARIFReport
	json.Unmarshal(out, &report)

	rules := report.Runs[0].Tool.Driver.Rules
	if len(rules) != 3 {
		t.Fatalf("Rules = %d, want 3", len(rules))
	}

	ruleIDs := map[string]bool{}
	for _, r := range rules {
		ruleIDs[r.ID] = true
	}
	expected := []string{"iampg/permission-detected", "iampg/wildcard-resource", "iampg/wildcard-action"}
	for _, id := range expected {
		if !ruleIDs[id] {
			t.Errorf("Missing rule %q", id)
		}
	}
}

func TestFormatDispatcher(t *testing.T) {
	doc := testDoc()

	formats := []Format{FormatJSON, FormatYAML, FormatTerraform, FormatSARIF}
	for _, f := range formats {
		t.Run(string(f), func(t *testing.T) {
			opts := map[string]string{"resource_name": "test", "version": "1.0.0"}
			out, err := doc.Format(f, opts)
			if err != nil {
				t.Fatalf("Format(%s) error: %v", f, err)
			}
			if len(out) == 0 {
				t.Errorf("Format(%s) returned empty output", f)
			}
		})
	}
}

