func TestToTerraformValid(t *testing.T) {
	doc := testDoc()
	out, err := doc.ToTerraform("my_policy")
	if err != nil {
		t.Fatalf("ToTerraform error: %v", err)
	}

	output := string(out)

	checks := []string{
		`resource "aws_iam_policy" "my_policy"`,
		`name        = "my_policy"`,
		`description = "Generated by iampg"`,
		`policy = <<-EOF`,
		`EOF`,
		`s3:GetObject`,
		`2012-10-17`,
	}
	for _, check := range checks {
		if !strings.Contains(output, check) {
			t.Errorf("Terraform output missing %q", check)
		}
	}
}

