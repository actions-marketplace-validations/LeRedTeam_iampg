func TestToTerraformContainsValidJSON(t *testing.T) {
	doc := testDoc()
	out, err := doc.ToTerraform("test")
	if err != nil {
		t.Fatalf("ToTerraform error: %v", err)
	}

	// Extract JSON between EOF markers
	output := string(out)
	start := strings.Index(output, "<<-EOF\n") + len("<<-EOF\n")
	end := strings.Index(output, "\n  EOF")
	if start < 0 || end < 0 || end <= start {
		t.Fatal("Could not find JSON between EOF markers")
	}

	jsonStr := strings.TrimSpace(output[start:end])
	var parsed map[string]interface{}
	if err := json.Unmarshal([]byte(jsonStr), &parsed); err != nil {
		t.Errorf("JSON inside Terraform heredoc is invalid: %v\nJSON: %s", err, jsonStr)
	}
}

func TestToSARIFValid(t *testing.T) {
	doc := testDoc()
	out, err := doc.ToSARIF("1.0.0")
	if err != nil {
		t.Fatalf("ToSARIF error: %v", err)
	}

	var report SARIFReport
	if err := json.Unmarshal(out, &report); err != nil {
		t.Fatalf("Output is not valid SARIF JSON: %v", err)
	}

	if report.Version != "2.1.0" {
		t.Errorf("SARIF version = %q, want 2.1.0", report.Version)
	}
	if len(report.Runs) != 1 {
		t.Fatalf("Runs = %d, want 1", len(report.Runs))
	}
	if report.Runs[0].Tool.Driver.Name != "iampg" {
		t.Errorf("Tool name = %q, want iampg", report.Runs[0].Tool.Driver.Name)
	}
	if report.Runs[0].Tool.Driver.Version != "1.0.0" {
		t.Errorf("Tool version = %q, want 1.0.0", report.Runs[0].Tool.Driver.Version)
	}
}

